function loop() {
  mapFind = canvas.getContext("2d");
  addEventListener("keydown", function(event) {
    keys[event.keyCode] = true;
  });

  addEventListener("keyup", function(event) {
    delete keys[event.keyCode];
  });

  function moveIt(e) {
    guy.x += guy.velX;
    guy.velX *= physics.friction;

    if (keys[68]) {
      if (guy.isJumping & (guy.velX < guy.speed)) {
        guy.velX++;
      }
    }

    if (keys[32]) {
      if (!guy.isJumping) {
        guy.isJumping = true;
        guy.velY = -guy.speed * 2;
        jumpSound.play();
      }
    }

    if (keys[65]) {
      if (guy.isJumping & (guy.velX > -guy.speed)) {
        guy.velX--;
      }
    }
  }

  function jump() {
    guy.velY += physics.gravity;
    guy.y += guy.velY;
    if (guy.velY === 0) {
      guy.isJumping = false;
    }
  }
  //collision position math
  const calculatedTileSize = 1.0 / mapTileSize;
  const nextX = guy.x + guy.velX;
  const nextY = guy.y + guy.velY;

  const currentMinX = (guy.x * calculatedTileSize) | 0;
  const currentMaxX = ((guy.x + guy.width) * calculatedTileSize) | 0;
  const currentMinY = (guy.y * calculatedTileSize) | 0;
  const currentMaxY = ((guy.y + guy.height) * calculatedTileSize) | 0;

  const nextMinX = (nextX * calculatedTileSize) | 0;
  const nextMaxX = ((nextX + guy.width) * calculatedTileSize) | 0;
  const nextMinY = (nextY * calculatedTileSize) | 0;
  const nextMaxY = ((nextY + guy.height) * calculatedTileSize) | 0;

  //is player in lava
  function level(mapArray) {
    for (let j = currentMinY; j <= currentMinY; j++) {
      for (let i = currentMinX; i <= currentMaxX; i++) {
        if (mapArray[j][i] === 2) {
          gameOver();
          burnSound.play();
          screamSound.play();
        }
      }
    }
    //is player in goldbox
    for (let j = currentMinY; j <= currentMaxY; j++) {
      for (let i = currentMinX; i <= currentMaxX; i++) {
        if (mapArray[j][i] === 3) {
          if (endLevel7 === false) {
            setTimeout(function() {
              levelcounter++;
            }, 50);
            clearTimeout();
          } else if ((levelcounter >= 30) & (levelcounter <= 33)) {
            mapFind.clearRect(0, 0, canvas.width, canvas.height);
            youWin();
          }
        }
      }
    }
    // collision Y axis
    if (guy.velX !== 0.0) {
      for (let i = nextMinX; i <= nextMaxX; i++) {
        for (let j = currentMinY; j <= currentMaxY; j++) {
          if (mapArray[j][i] === 1) {
            guy.velX = 0.0;
          }
        }
      }
    }

    // collision X axis
    if (guy.velY !== 0.0) {
      for (let j = nextMinY; j <= nextMaxY; j++) {
        for (let i = nextMinX; i <= currentMaxX; i++) {
          if (mapArray[j][i] === 1) {
            guy.velY = 0.0;
            physics.gravity = 0;
          } else {
            if ((mapArray[j][i] === 0, 3)) {
              physics.gravity = 0.4;
            }
          }
        }
      }
    }
//map draw code
    for (let j = 0; j < mapArray.length; j++) {
      for (let i = 0; i < mapArray[0].length; i++) {
        if (map[j][i] === 2) {
          mapFind.clearRect(
            i * mapTileSize,
            j * mapTileSize,
            mapTileSize,
            mapTileSize
          );
          mapFind.fillStyle = "darkred";
          mapFind.fillRect(
            i * mapTileSize,
            j * mapTileSize,
            mapTileSize,
            mapTileSize
          );
        } else if (mapArray[j][i] === 1) {
          mapFind.fillStyle = "gray";
          mapFind.clearRect(
            i * mapTileSize,
            j * mapTileSize,
            mapTileSize,
            mapTileSize
          );
          mapFind.fillRect(
            i * mapTileSize,
            j * mapTileSize,
            mapTileSize,
            mapTileSize
          );
        } else if (mapArray[j][i] === 3) {
          mapFind.fillStyle = "rgba(100%, 100%, 0%, 0.76)";
          mapFind.clearRect(
            i * mapTileSize,
            j * mapTileSize,
            mapTileSize,
            mapTileSize
          );
          mapFind.fillRect(
            i * mapTileSize,
            j * mapTileSize,
            mapTileSize,
            mapTileSize
          );
        } else if (mapArray[j][i] === 0) {
          mapFind.fillStyle = "black";
          mapFind.clearRect(
            i * mapTileSize,
            j * mapTileSize,
            mapTileSize,
            mapTileSize
          );
          mapFind.fillRect(
            i * mapTileSize,
            j * mapTileSize,
            mapTileSize,
            mapTileSize
          );
        }
      }
    }
  }

  guy.x = guy.x + guy.velX;
  guy.y = guy.y + guy.velY;

  if (levelcounter === 1) {
    level(map)
    howTo() ;
  } else if ((levelcounter >= 2) & (levelcounter < 7)) {
    level(map2);
    isJumping = true;
    if (endLevel1 != true) {
      resetGuy();
    }
    endLevel1 = true;
  } else if ((levelcounter >= 7) & (levelcounter <= 11)) {
    level(map3);
    if (endLevel2 != true) {
      resetGuy();
    }
    endLevel2 = true;
  } else if ((levelcounter >= 12) & (levelcounter <= 15)) {
    level(map4);
    if (endLevel3 != true) {
      resetGuy();
    }
    endLevel3 = true;
  } else if ((levelcounter >= 16) & (levelcounter <= 19)) {
    // mapFind.clearRect(0, 0, width, height);
    level(map5);
    if (endLevel4 != true) {
      resetGuy();
    }
    endLevel4 = true;
  } else if ((levelcounter >= 20) & (levelcounter < 25)) {
    level(map6);
    if (endLevel5 != true) {
      resetGuy();
    }
    endLevel5 = true;
  } else if ((levelcounter >= 25) & (levelcounter < 29)) {
    level(map7);
    if (endLevel6 != true) {
      resetGuy();
    }
    endLevel6 = true;
  } else if ((levelcounter >= 29) & (levelcounter <= 33)) {
    level(map8);
    if (endLevel7 != true) {
      resetGuy();
    }
    endLevel7 = true;
  }

  mapFind.fillRect(guy.x, guy.y, guy.width, guy.height);
  mapFind.clearRect(guy.x, guy.y, guy.width, guy.height);
  requestAnimationFrame(loop);
  moveIt();
  jump();
  for (let j = currentMinY; j <= currentMaxY; j++) {
    for (let i = currentMinX; i <= currentMaxX; i++) {
      if (map7[j][i] === 3) {
        if ((levelcounter >= 30) & (levelcounter <= 33)) {
          youWin();
        }
      }
    }
  }
}
